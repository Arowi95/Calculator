# calculadora.py

def mostrar_menu():
    print("\n--- Calculadora B√°sica ---")
    print("1. Sumar")
    print("2. Restar")
    print("3. Multiplicar")
    print("4. Dividir")
    print("5. Salir")

def pedir_numero(mensaje):
    while True:
        try:
            return float(input(mensaje))
        except ValueError:
            print("‚ùå Error: Ingresa un n√∫mero v√°lido.")

def calculadora():
    while True:
        mostrar_menu()
        opcion = input("Selecciona una opci√≥n (1-5): ")

        if opcion == "5":
            print("üëã Gracias por usar la calculadora.")
            break

        if opcion not in ["1", "2", "3", "4"]:
            print("‚ùå Opci√≥n inv√°lida. Intenta de nuevo.")
            continue

        num1 = pedir_numero("Ingresa el primer n√∫mero: ")
        num2 = pedir_numero("Ingresa el segundo n√∫mero: ")

        if opcion == "1":
            resultado = num1 + num2
            print(f"‚úÖ Resultado: {num1} + {num2} = {resultado}")
        elif opcion == "2":
            resultado = num1 - num2
            print(f"‚úÖ Resultado: {num1} - {num2} = {resultado}")
        elif opcion == "3":
            resultado = num1 * num2
            print(f"‚úÖ Resultado: {num1} * {num2} = {resultado}")
        elif opcion == "4":
            if num2 == 0:
                print("‚ùå Error: No se puede dividir entre cero.")
            else:
                resultado = num1 / num2
                print(f"‚úÖ Resultado: {num1} / {num2} = {resultado}")

if __name__ == "__main__":
    calculadora()